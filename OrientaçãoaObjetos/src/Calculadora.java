
public class Calculadora {

	public static void main(String[] args) {
		
		double n1 = 5;
		double n2 = 2;
		double resulSub;
		double resulSo;
		double resulMu;
		double resulDiv;
		

		resulSo = n1 + n2;
		System.out.println("O Resultado da soma é " + resulSo);
		
		resulSub = n1 - n2;
		System.out.println("O Resultado da subtração é " + resulSub);
		
		resulMu = n1 * n2;
		System.out.println("O Resultado da multiplicação é " + resulMu);
		
		resulDiv = n1 / n2;
		System.out.println("O Resultado da divisão é " + resulDiv);
		
		
	}

}
